create table Person (
BusinessEntityID int,
PersonType nvarchar(2),
NameStyle int,
Title varchar(6),
FirstName nvarchar(20),
MiddleName nvarchar20),
LastName nvarchar(20),   
Suffix varchar(6),
EmailPromotion nvarchar(10),
AdditionalContactInfo nvarchar,  
Demographics nvarchar,   
rowguid nvarchar(50),
ModifiedDate smalldatetime,
PRIMARY KEY(BusinessEntityID)
);
